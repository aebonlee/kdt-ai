// ─────────────────────────────────────────────────────────────
// 강의 데이터 분할 코드모드 (성능: Lectures 청크 경량화)
//
// 진실 원본은 src/data/lecture*.js (모놀리식, 여기서 편집).
// 이 스크립트가 subjectId 별로 묶어 src/data/lectures/<subjectId>.js 를 생성한다.
// Lectures.jsx 는 현재 과목 모듈만 동적 import → 첫 진입 시 미사용 과목 데이터 제외.
//
// 실행: npm run split (predev/prebuild 로 자동 실행). 생성물은 .gitignore.
// ─────────────────────────────────────────────────────────────
import { writeFileSync, mkdirSync, rmSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { periods, periodsFor } from '../src/data/lectureperiods.js'
import { planFor } from '../src/data/lectureplans.js'
import { examplesFor } from '../src/data/lectureexamples.js'
import { conceptsFor } from '../src/data/lectureconcepts.js'
import { detailsFor } from '../src/data/lecturedetails.js'
import { theoryFor } from '../src/data/lecturetheory.js'
import { realCodeExtraFor } from '../src/data/lecturetheory2.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = join(__dirname, '../src/data/lectures')

// 모든 "<subjectId>-<day>" 키 (하이픈 포함 subjectId 안전: 마지막 -숫자만 day)
const keys = Object.keys(periods)
const bySubject = {}
for (const key of keys) {
  const m = key.match(/^(.+)-(\d+)$/)
  if (!m) continue
  const sid = m[1]
  const day = Number(m[2])
  const th = theoryFor(sid, day)
  const dayData = {
    plan: planFor(sid, day),
    examples: examplesFor(sid, day), // examples + examplesExtra 병합됨
    concepts: conceptsFor(sid, day),
    detail: detailsFor(sid, day),
    theory: th ? { theory: th.theory || [] } : null, // deepTheory.theory 만 사용, realCode 는 아래 realCodes 로
    realCodes: [...(th?.realCode || []), ...realCodeExtraFor(sid, day)],
    periods: periodsFor(sid, day),
  }
  ;(bySubject[sid] ||= {})[key] = dayData
}

rmSync(OUT, { recursive: true, force: true })
mkdirSync(OUT, { recursive: true })
let n = 0
for (const [sid, data] of Object.entries(bySubject)) {
  writeFileSync(
    join(OUT, `${sid}.js`),
    `// AUTO-GENERATED by scripts/split-lectures.mjs — DO NOT EDIT (edit src/data/lecture*.js instead)\nexport default ${JSON.stringify(data)}\n`,
  )
  n++
}
console.log(`split-lectures: ${keys.length} keys → ${n} subject modules in src/data/lectures/`)
